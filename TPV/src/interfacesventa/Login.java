    private void checkCorrectPassword () {
        try {
            Connection conexion = Agente.getAgente().mBD;
            String usuario = txtUsuario.getText();
            String password = new String(txtPassword.getPassword());
                
            String sql = "SELECT ID_USUARIO FROM USUARIOS WHERE USERNAME='"+usuario+"' AND PASSWORD='"+password+"'";
            Statement sentencia = conexion.createStatement();
            ResultSet result = sentencia.executeQuery(sql);
            
            if (result.next()) {
                principal ventana = new principal();
                ventana.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog (null, "La contrase√±a es incorrecta","Advertencia",JOptionPane.ERROR_MESSAGE);
                txtUsuario.setText("");
                txtPassword.setText("");
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }
    
    
